package Service.user;

public class Manager extends User {


    private String Manager_Name;
    private String Manager_Sex;
    private String Manager_Tel;




    public String getManager_Name() {
        return Manager_Name;
    }

    public void setManager_Name(String ManagerName) {
        Manager_Name = ManagerName;
    }

    public String getManager_Sex() {
        return Manager_Sex;
    }

    public void setManager_Sex(String ManagerSex) {
        Manager_Sex = ManagerSex;
    }

    public String getManager_Tel() {
        return Manager_Tel;
    }

    public void setManager_Tel(String ManagerTel) {
        Manager_Tel = ManagerTel;
    }

}





